package com.example.calculator

class Calculator() {

    fun add(currentTotal : Float, valueToAdd: Float) = currentTotal + valueToAdd
    fun subtract (currentTotal : Float, valueToSubtract: Float) = currentTotal - valueToSubtract
    fun multiply (currentTotal: Float, valueToMultiply: Float) = currentTotal * valueToMultiply
    fun divide (currentTotal: Float, valueToDivide: Float) = currentTotal / valueToDivide

}
